-- =============================================
-- 网约车派单调度仿真系统 - 初始化数据脚本
-- =============================================

-- 建表语句
-- 1. 停车场表
CREATE TABLE IF NOT EXISTS parking_lot (
    id VARCHAR(10) PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    gis_grid VARCHAR(20) NOT NULL,
    capacity INTEGER NOT NULL,
    latitude DOUBLE NOT NULL,
    longitude DOUBLE NOT NULL,
    remark VARCHAR(255)
);

-- 2. 站点表
CREATE TABLE IF NOT EXISTS station (
    id VARCHAR(15) PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    latitude DOUBLE NOT NULL,
    longitude DOUBLE NOT NULL,
    type VARCHAR(20) NOT NULL,
    remark VARCHAR(255)
);

-- 3. 车辆表
CREATE TABLE IF NOT EXISTS vehicle (
    id VARCHAR(10) PRIMARY KEY,
    vin VARCHAR(17) NOT NULL UNIQUE,
    status VARCHAR(20) NOT NULL,
    order_id VARCHAR(30),
    battery_percent INTEGER NOT NULL,
    latitude DOUBLE NOT NULL,
    longitude DOUBLE NOT NULL,
    gis_grid VARCHAR(20) NOT NULL,
    target_station_id VARCHAR(15),
    dispatch_path TEXT,
    dispatch_progress INTEGER,
    dispatch_start_time TIMESTAMP,
    pickup_path TEXT,
    pickup_progress INTEGER,
    delivery_path TEXT,
    delivery_progress INTEGER,
    dispatch_order_start_time TIMESTAMP
);

-- 4. 订单表
CREATE TABLE IF NOT EXISTS "order" (
    id VARCHAR(30) PRIMARY KEY,
    origin_station_id VARCHAR(15) NOT NULL,
    destination_station_id VARCHAR(15) NOT NULL,
    start_time TIMESTAMP NOT NULL,
    estimated_arrival_time TIMESTAMP NOT NULL,
    estimated_revenue DOUBLE NOT NULL,
    status VARCHAR(20) NOT NULL,
    vehicle_id VARCHAR(10),
    actual_arrival_time TIMESTAMP,
    pickup_start_time TIMESTAMP,
    pickup_arrival_time TIMESTAMP
);

